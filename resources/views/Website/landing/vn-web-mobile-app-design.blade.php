        function sendEmail() {
            var name = $("#name");
            var email = $("#email");
            var address = $("#address");
            var company = $("#company");

            if (isNotEmpty(name) && isNotEmpty(email) && isNotEmpty(address) && isNotEmpty(company)) {
                $('.slide-btn').button('loading');
                $.ajax({
                    url: 'online-seminar',
                    method: 'POST',
                    dataType: 'json',
                    data: {
                        "_token": "{{ csrf_token() }}",
                        name: name.val(),
                        email: email.val(),
                        address: address.val(),
                        company: company.val()
                    }, success: function (response) {
                        swal("", "Cảm ơn bạn đã đăng ký, chúng tôi sẽ gửi cho bạn link URL buổi seminar qua email bạn đã đăng ký vào buổi tối trước ngày seminar được diễn ra (07/09/2020)", "success", { button: false });
                        $('#myForm')[0].reset();
                        $('.modal').hide();
                        $('.slide-btn').button('reset');
                        // alert('Cảm ơn bạn đã đăng ký, chúng tôi sẽ gửi cho bạn link URL buổi seminar qua email bạn đã đăng ký vào buổi tối trước ngày seminar được diễn ra (19/08/2020)')

                    }
                });
            }
        }

        function sendSuccess() {
            var nameSuccess = $("#nameSuccess");
            var emailSuccess = $("#emailSuccess");
            var msg_subject = $("#msg_subject");
            var message = $("#message");
            if (isNotEmpty(nameSuccess) && isNotEmpty(emailSuccess) && isNotEmpty(msg_subject) && isNotEmpty(message)) {
                $('.contact-btn1').button('loading');
                $.ajax({
                    url: 'online-seminar-success',
                    method: 'POST',
                    dataType: 'json',
                    data: {
                        "_token": "{{ csrf_token() }}",
                        nameSuccess: nameSuccess.val(),
                        emailSuccess: emailSuccess.val(),
                        msg_subject: msg_subject.val(),
                        message: message.val()
                    }, success: function (response) {
                        swal("", "Thư của bạn đã được gửi thành công, cảm ơn bạn đã liên hệ với chúng tôi", "success");
                        $('#contactForm')[0].reset();
                        $('.contact-btn1').button('reset');
                        // $('.modal').modal('hide');
                    }
                });
            }
        }

        function isNotEmpty(caller) {
            if (caller.val() == "") {
                caller.css('border', '1px solid red');
                return false;
            } else
                caller.css('border', '');

            return true;
        }

